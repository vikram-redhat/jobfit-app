-- supabase-tool-results-migration.sql
-- Adds the tool_results table that backs the anonymous free tools at
-- /tools/job-description-keyword-extractor and /tools/resume-grader.
--
-- Each row is a permalink for a tool result, e.g.
--   /tools/job-description-keyword-extractor/r/<id>
-- These pages are PUBLIC and INDEXABLE — that's the SEO unlock.
-- Old results are pruned by a cron job (see bottom).
--
-- Run this in the Supabase SQL editor.

create table if not exists public.tool_results (
  id              uuid primary key default gen_random_uuid(),
  tool            text not null check (tool in ('keywords', 'resume-grader')),
  -- Short, human-readable label used in the page title. e.g. "Senior Marketing
  -- Coordinator at Spotify" or "Software Engineer resume". Generated by the AI.
  display_title   text,
  -- The model's structured response. Schema varies per tool.
  result          jsonb not null,
  -- A short excerpt of the input (first ~200 chars) so the result page can
  -- show users "you analyzed: ..." without exposing the full pasted text.
  input_excerpt   text,
  created_at      timestamptz not null default now(),
  expires_at      timestamptz not null default (now() + interval '30 days')
);

-- Lookup index — these pages are fetched by id on every page view.
create index if not exists idx_tool_results_id on public.tool_results(id);
create index if not exists idx_tool_results_expires on public.tool_results(expires_at);

-- Row Level Security: enable RLS, then allow anon SELECT on non-expired rows
-- and anon INSERT (so the API route can create results without service role
-- credentials). UPDATE and DELETE are not granted to anon.
alter table public.tool_results enable row level security;

drop policy if exists "Anonymous can read non-expired results"  on public.tool_results;
drop policy if exists "Anonymous can insert results"            on public.tool_results;

create policy "Anonymous can read non-expired results"
  on public.tool_results
  for select
  using (expires_at > now());

create policy "Anonymous can insert results"
  on public.tool_results
  for insert
  with check (true);

